def random_meal_selection():
    """
    随机选择5个菜系，从每个菜系中随机选出一道菜
    返回格式为：[菜系-菜名, 菜系-菜名, ...]
    """
    selected_cuisines = random.sample(list(all_cuisines.keys()), 5)

    result = [
        f"{cuisine_type}-{random.choice(all_cuisines[cuisine_type])}"
        for cuisine_type in selected_cuisines
    ]

    return result
